def draw_card():
    return min(np.random.randint(1, 14), 10)

def hand_value(hand):
    total = sum(hand)
    if 1 in hand and total + 10 <= 21:
        return total + 10, True
    return total, False

def play_episode(policy=None, epsilon=0.0):
    player = [draw_card(), draw_card()]
    dealer = [draw_card(), draw_card()]
    states_actions = []

    while True:
        p_sum, usable = hand_value(player)
        state = (p_sum, dealer[0], usable)
        if p_sum >= 21:
            break
        if policy is None or np.random.random() < epsilon:
            action = np.random.randint(2)
        else:
            action = policy.get(state, 1)
        states_actions.append((state, action))
        if action == 0:
            break
        player.append(draw_card())

    while hand_value(dealer)[0] < 17:
        dealer.append(draw_card())

    p_final, d_final = hand_value(player)[0], hand_value(dealer)[0]

    if   p_final > 21:               reward = -1
    elif d_final > 21 or p_final > d_final: reward = +1
    elif p_final == d_final:         reward =  0
    else:                            reward = -1

    return states_actions, reward

# ─────────────────────────────────────────
