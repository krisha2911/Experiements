# ─────────────────────────────────────────
# METHOD 1 — Linear Regression
# ─────────────────────────────────────────

def run_linear(X_train, y_train, X_test, y_test, epochs):
    lr = 0.01
    W  = np.zeros((X_train.shape[1], 1))
    losses = []
    for _ in range(epochs):
        pred  = X_train @ W
        error = pred - y_train
        W    -= lr * (X_train.T @ error) / len(y_train)
        losses.append(float(np.mean(error**2)))
    pred_test = X_test @ W
    # Denormalize
    pred_out = pred_test * y_std + y_mean
    true_out = y_test    * y_std + y_mean
    mse = float(np.mean((pred_out - true_out)**2))
    mae = float(np.mean(np.abs(pred_out - true_out)))
    return pred_out, losses, mse, mae

# ─────────────────────────────────────────
# METHOD 2 — Shallow Neural Network
# ─────────────────────────────────────────

def relu(x):   return np.maximum(0, x)
def relu_d(x): return (x > 0).astype(float)

def clip(g, threshold=1.0):
    """Gradient clipping — prevents exploding gradients."""
    norm = np.linalg.norm(g)
    return g * (threshold / norm) if norm > threshold else g

def run_neural(X_train, y_train, X_test, y_test, epochs, hidden=16):
    lr   = 0.001          # smaller LR for neural net
    n_in = X_train.shape[1]

    # Xavier initialization
    W1 = np.random.randn(n_in, hidden) * np.sqrt(2.0 / n_in)
    b1 = np.zeros((1, hidden))
    W2 = np.random.randn(hidden, 1)   * np.sqrt(2.0 / hidden)
    b2 = np.zeros((1, 1))

    losses = []
    for _ in range(epochs):
        # Forward
        z1   = X_train @ W1 + b1
        a1   = relu(z1)
        pred = a1 @ W2 + b2

        error = pred - y_train
        losses.append(float(np.mean(error**2)))

        # Backward with gradient clipping
        d_out = 2 * error / len(y_train)
        dW2   = clip(a1.T @ d_out)
        db2   = d_out.sum(axis=0, keepdims=True)
        d_h   = (d_out @ W2.T) * relu_d(z1)
        dW1   = clip(X_train.T @ d_h)
        db1   = d_h.sum(axis=0, keepdims=True)

        W2 -= lr * dW2;  b2 -= lr * db2
        W1 -= lr * dW1;  b1 -= lr * db1

    # Test
    a1_test   = relu(X_test @ W1 + b1)
    pred_test = a1_test @ W2 + b2

    # Denormalize
    pred_out = pred_test * y_std + y_mean
    true_out = y_test    * y_std + y_mean
    mse = float(np.mean((pred_out - true_out)**2))
    mae = float(np.mean(np.abs(pred_out - true_out)))
    return pred_out, losses, mse, mae

