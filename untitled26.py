CHARGERS = set()
charger_defaults = ["0 3", "2 1"]
for i in range(num_chargers):
    CHARGERS.add(get_pos(f"  Charger {i+1}", charger_defaults[i] if i < 2 else "0 0", ROWS, COLS))

num_obstacles = int(ask("Number of obstacles", 2))
OBSTACLES = set()
obs_defaults = ["1 2", "2 3"]
for i in range(num_obstacles):
    OBSTACLES.add(get_pos(f"  Obstacle {i+1}", obs_defaults[i] if i < 2 else "1 1", ROWS, COLS))

# ─────────────────────────────────────────
# MDP
# ─────────────────────────────────────────

MOVES        = [(-1,0),(1,0),(0,-1),(0,1)]
ACTION_NAMES = ["Up","Down","Left","Right"]
NUM_ACTIONS  = 4
THETA        = 1e-6

def is_valid(r, c):
    return 0 <= r < ROWS and 0 <= c < COLS and (r, c) not in OBSTACLES

def get_transitions(r, c, a):
    """Returns list of (nr, nc, prob) — probabilistic wind drift."""
    sideways = [MOVES[i] for i in range(NUM_ACTIONS) if i != a]
    p_side   = (1 - P_INTENDED) / len(sideways)

    raw = [(MOVES[a], P_INTENDED)] + [(s, p_side) for s in sideways]

    merged = {}
    for move, prob in raw:
        nr = r + move[0]
        nc = c + move[1]
        if not is_valid(nr, nc):
            nr, nc = r, c    # bounce back
        key = (nr, nc)
        merged[key] = merged.get(key, 0) + prob

    # Return as (nr, nc, prob)
    return [(nr, nc, prob) for (nr, nc), prob in merged.items()]

def get_reward(nr, nc, new_bat):
    if (nr, nc) == DELIVERY: return +20
    if new_bat == 0:          return -10
    if (nr, nc) in CHARGERS:  return  +2
    return -1

def is_terminal(r, c, bat):
    return (r, c) == DELIVERY or bat == 0

def next_battery(nr, nc, bat):
    if (nr, nc) in CHARGERS:
        return min(bat + 3, MAX_BATTERY)
    return max(bat - 1, 0)

# ─────────────────────────────────────────
# VALUE ITERATION
# ─────────────────────────────────────────

print("\nRunning Value Iteration...")
V = np.zeros((ROWS, COLS, MAX_BATTERY + 1))

vi_iter = 0
while True:
    delta = 0
    for r in range(ROWS):
        for c in range(COLS):
            if (r, c) in OBSTACLES:
                continue
            for bat in range(MAX_BATTERY + 1):
                if is_terminal(r, c, bat):
                    V[r, c, bat] = get_reward(r, c, bat)
                    continue
                best = -np.inf
                for a in range(NUM_ACTIONS):
                    val = 0
                    for nr, nc, prob in get_transitions(r, c, a):
                        nb  = next_battery(nr, nc, bat)
                        val += prob * (get_reward(nr, nc, nb) + GAMMA * V[nr, nc, nb])
                    best = max(best, val)
                delta        = max(delta, abs(best - V[r, c, bat]))
                V[r, c, bat] = best
    vi_iter += 1
    if delta < THETA:
        break

print(f"  Converged in {vi_iter} iterations.")

# Extract VI policy at full battery
vi_policy = np.zeros((ROWS, COLS), dtype=int)
for r in range(ROWS):
    for c in range(COLS):
        if (r, c) in OBSTACLES or is_terminal(r, c, MAX_BATTERY):
            continue
        best_a, best_v = 0, -np.inf
        for a in range(NUM_ACTIONS):
            val = 0
            for nr, nc, prob in get_transitions(r, c, a):
                nb  = next_battery(nr, nc, MAX_BATTERY)
                val += prob * (get_reward(nr, nc, nb) + GAMMA * V[nr, nc, nb])
            if val > best_v:
                best_v, best_a = val, a
        vi_policy[r, c] = best_a

# ─────────────────────────────────────────
# POLICY ITERATION
# ─────────────────────────────────────────

print("Running Policy Iteration...")
policy = np.zeros((ROWS, COLS, MAX_BATTERY + 1), dtype=int)
V2     = np.zeros((ROWS, COLS, MAX_BATTERY + 1))

pi_iter = 0
while True:
    # Policy Evaluation
    while True:
        delta = 0
        for r in range(ROWS):
            for c in range(COLS):
                if (r, c) in OBSTACLES:
                    continue
                for bat in range(MAX_BATTERY + 1):
                    if is_terminal(r, c, bat):
                        V2[r, c, bat] = get_reward(r, c, bat)
                        continue
                    val = 0
                    for nr, nc, prob in get_transitions(r, c, policy[r, c, bat]):
                        nb  = next_battery(nr, nc, bat)
                        val += prob * (get_reward(nr, nc, nb) + GAMMA * V2[nr, nc, nb])
                    delta        = max(delta, abs(val - V2[r, c, bat]))
                    V2[r, c, bat] = val
        if delta < THETA:
            break

    # Policy Improvement
    stable = True
    for r in range(ROWS):
        for c in range(COLS):
            if (r, c) in OBSTACLES:
                continue
            for bat in range(MAX_BATTERY + 1):
                if is_terminal(r, c, bat):
                    continue
                old_a = policy[r, c, bat]
                best_a, best_v = 0, -np.inf
                for a in range(NUM_ACTIONS):
                    val = 0
                    for nr, nc, prob in get_transitions(r, c, a):
                        nb  = next_battery(nr, nc, bat)
                        val += prob * (get_reward(nr, nc, nb) + GAMMA * V2[nr, nc, nb])
                    if val > best_v:
                        best_v, best_a = val, a
                policy[r, c, bat] = best_a
                if old_a != best_a:
                    stable = False
    pi_iter += 1
    if stable:
        break

print(f"  Converged in {pi_iter} iterations.")

