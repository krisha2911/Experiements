# ─────────────────────────────────────────
# POLICY EVALUATION
# ─────────────────────────────────────────

def policy_evaluation(policy, P, R, gamma):
    V = np.zeros(NUM_STATES)
    while True:
        delta = 0
        for s in range(NUM_STATES):
            a = policy[s]
            v = sum(P[s][a][s2] * (R[s][a] + gamma * V[s2]) for s2 in range(NUM_STATES))
            delta = max(delta, abs(v - V[s]))
            V[s] = v
        if delta < 1e-6:
            break
    return V

# ─────────────────────────────────────────
# POLICY IMPROVEMENT
# ─────────────────────────────────────────

def policy_improvement(V, P, R, gamma):
    new_policy = np.zeros(NUM_STATES, dtype=int)
    for s in range(NUM_STATES):
        q = [sum(P[s][a][s2] * (R[s][a] + gamma * V[s2]) for s2 in range(NUM_STATES))
             for a in range(NUM_ACTIONS)]
        new_policy[s] = np.argmax(q)
    return new_policy

# ─────────────────────────────────────────
# POLICY ITERATION LOOP
# ─────────────────────────────────────────

