gamma   = 0.9   # Discount factor
epsilon = 0.2   # Exploration rate
episodes = 500

# ─────────────────────────────────────────
# ENVIRONMENT
# ─────────────────────────────────────────

def step(state, action):
    move = actions[action]
    next_state = (state[0] + move[0], state[1] + move[1])

    # Stay in place if out of bounds
    if next_state[0] < 0 or next_state[0] >= grid_size or \
       next_state[1] < 0 or next_state[1] >= grid_size:
        next_state = state

    if next_state == goal_state:
        return next_state, +10, True
    else:
        return next_state, -1, False

# ─────────────────────────────────────────
# PART 1 — TD(0) Prediction
# ─────────────────────────────────────────

V = np.zeros((grid_size, grid_size))

for ep in range(episodes):
    state = start_state
    for _ in range(200):
        action = random.randint(0, 3)           # Random policy
        next_state, reward, done = step(state, action)

        # TD(0) update
        td_target = reward + gamma * V[next_state[0]][next_state[1]]
        td_error  = td_target - V[state[0]][state[1]]
        V[state[0]][state[1]] += alpha * td_error

        state = next_state
        if done:
            break

print("TD(0) State Value Table (V under random policy):")
print(np.round(V, 2))

