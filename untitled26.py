# HELPER — Pull an arm
# ─────────────────────────────────────────

def pull(arm):
    return 1 if np.random.random() < TRUE_PROBS[arm] else 0

# ─────────────────────────────────────────
# STRATEGY 1 — Epsilon-Greedy
# ─────────────────────────────────────────

counts = np.zeros(NUM_ARMS)
values = np.zeros(NUM_ARMS)
eg_rewards = []

for t in range(NUM_STEPS):
    arm = np.random.randint(NUM_ARMS) if np.random.random() < EPSILON else np.argmax(values)
    r = pull(arm)
    counts[arm] += 1
    values[arm] += (r - values[arm]) / counts[arm]
    eg_rewards.append(r)

# ─────────────────────────────────────────
# STRATEGY 2 — UCB
# ─────────────────────────────────────────

counts = np.zeros(NUM_ARMS)
values = np.zeros(NUM_ARMS)
ucb_rewards = []

for t in range(1, NUM_STEPS + 1):
    arm = t - 1 if t <= NUM_ARMS else np.argmax(values + np.sqrt(2 * np.log(t) / (counts + 1e-9)))
    r = pull(arm)
    counts[arm] += 1
    values[arm] += (r - values[arm]) / counts[arm]
    ucb_rewards.append(r)

# ─────────────────────────────────────────
# STRATEGY 3 — Softmax
# ─────────────────────────────────────────

counts = np.zeros(NUM_ARMS)
values = np.zeros(NUM_ARMS)
sm_rewards = []

for t in range(NUM_STEPS):
    exp_v = np.exp(values / TEMPERATURE)
    probs = exp_v / np.sum(exp_v)
    arm = np.random.choice(NUM_ARMS, p=probs)
    r = pull(arm)
    counts[arm] += 1
    values[arm] += (r - values[arm]) / counts[arm]
    sm_rewards.append(r)

