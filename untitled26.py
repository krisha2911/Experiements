import numpy as np
import random

# ─────────────────────────────────────────
# 1. GRID-WORLD ENVIRONMENT
# ─────────────────────────────────────────

# Grid layout (4x4):
#  S  .  .  .
#  .  X  .  X
#  .  .  .  X
#  X  .  .  G
#
#  S = Start (0,0), G = Goal (3,3), X = Hole (penalty)

GRID_SIZE   = 4
START_STATE = (1, 0)
GOAL_STATE  = (3, 0)
HOLES       = {(1, 1), (1, 3), (2, 3)}  # instant penalty cells

# Actions: 0=Up, 1=Down, 2=Left, 3=Right
ACTIONS      = [0, 1, 2, 3]
ACTION_NAMES = ["Up", "Down", "Left", "Right"]

def step(state, action):
    """Take an action in the environment, return (next_state, reward, done)."""
    r, c = state

    if   action == 0: r -= 1   # Up
    elif action == 1: r += 1   # Down
    elif action == 2: c -= 1   # Left
    elif action == 3: c += 1   # Right

    # Clamp within grid boundaries
    r = max(0, min(GRID_SIZE - 1, r))
    c = max(0, min(GRID_SIZE - 1, c))

    next_state = (r, c)

    # Assign reward
    if next_state == GOAL_STATE:
        return next_state, +10, True   # Reached goal
    elif next_state in HOLES:
        return next_state, -5,  True   # Fell in hole
    else:
        return next_state, -1,  False  # Normal step penalty


# ─────────────────────────────────────────
# 2. Q-LEARNING ALGORITHM
# ─────────────────────────────────────────

# Hyperparameters
EPISODES    = 500      # Total training episodes
ALPHA       = 0.1      # Learning rate
GAMMA       = 0.9      # Discount factor
EPSILON     = 1.0      # Initial exploration rate
EPSILON_MIN = 0.01     # Minimum exploration rate
EPSILON_DECAY = 0.99   # Decay per episode

# Initialize Q-table with zeros: shape = (rows, cols, num_actions)
Q = np.zeros((GRID_SIZE, GRID_SIZE, len(ACTIONS)))

rewards_per_episode = []

for episode in range(EPISODES):
    state   = START_STATE
    total_reward = 0

    for _ in range(100):  # Max 100 steps per episode
        r, c = state

        # ── ε-greedy action selection ──
        if random.uniform(0, 1) < EPSILON:
            action = random.choice(ACTIONS)           # Explore
        else:
            action = np.argmax(Q[r, c])               # Exploit

        # ── Take action ──
        next_state, reward, done = step(state, action)
        nr, nc = next_state

        # ── Q-value update (Bellman equation) ──
        old_value  = Q[r, c, action]
        future_max = np.max(Q[nr, nc])
        Q[r, c, action] = old_value + ALPHA * (reward + GAMMA * future_max - old_value)

        state        = next_state
        total_reward += reward

        if done:
            break

    # Decay epsilon after each episode
    EPSILON = max(EPSILON_MIN, EPSILON * EPSILON_DECAY)
    rewards_per_episode.append(total_reward)


# ─────────────────────────────────────────
# 3. RESULTS
# ─────────────────────────────────────────

print("=" * 40)
print("   Q-LEARNING TRAINING COMPLETE")
print("=" * 40)

# Average reward over last 100 episodes
avg = np.mean(rewards_per_episode[-100:])
print(f"\nAvg reward (last 100 episodes): {avg:.2f}")

# ── Learned policy ──
print("\nLearned Policy (Best Action per Cell):")
print("  " + "  ".join([f" {c}" for c in range(GRID_SIZE)]))
for r in range(GRID_SIZE):
    row = ""
    for c in range(GRID_SIZE):
        if (r, c) == GOAL_STATE:
            symbol = " G"
        elif (r, c) in HOLES:
            symbol = " X"
        else:
            best = np.argmax(Q[r, c])
            arrows = ["↑", "↓", "←", "→"]
            symbol = f" {arrows[best]}"
        row += symbol + " "
    print(f"{r} {row}")

# ── Test: run one greedy episode ──
print("\n--- Test Run (Greedy Policy) ---")
state = START_STATE
path  = [state]
for _ in range(20):
    r, c   = state
    action = np.argmax(Q[r, c])
    state, reward, done = step(state, action)
    path.append(state)
    if done:
        break

print("Path taken:", " → ".join(str(s) for s in path))
if path[-1] == GOAL_STATE:
    print("✅ Agent reached the GOAL!")
else:
    print("❌ Agent did not reach the goal.")



