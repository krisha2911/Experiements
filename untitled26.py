print("=" * 50)

ROWS        = int(ask("\nGrid rows",    4))
COLS        = int(ask("Grid cols",     4))
MAX_BATTERY = int(ask("Max battery",  10))
GAMMA       = float(ask("Gamma",     0.9))
P_INTENDED  = float(ask("Prob intended move succeeds", 0.7))

print()
START    = get_pos("Start position",           "0 0",                 ROWS, COLS)
DELIVERY = get_pos("Delivery (goal) position", f"{ROWS-1} {COLS-1}", ROWS, COLS)

